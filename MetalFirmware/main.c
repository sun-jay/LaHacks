void parse_input_chars()
{
	char ca1[4];
	char ca2[4];
	char ca3[4];

	char cmag = '0';

	//break this up:

	//					  0123456789 10 11  12
	//char rec_arr[13] = "000 000 00 0     0";


	ca1[0] = rec_arr[0];
	ca1[1] = rec_arr[1];
	ca1[2] = rec_arr[2];
	ca1[3] = '\0';

	ca2[0] = rec_arr[4];
	ca2[1] = rec_arr[5];
	ca2[2] = rec_arr[6];
	ca2[3] = '\0';


	ca3[0] = rec_arr[8];
	ca3[1] = rec_arr[9];
	ca3[2] = rec_arr[10];
	ca3[3] = '\0';


	a1 = atoi(ca1);
	a2 = atoi(ca2);
	a3 = atoi(ca3);

	mag = atoi(cmag);


}


void move_motors()
{
	// change channel duty cycle by changing TIM1->CCRx Register Value
	// 5%  DC ---   1000  ---  0   deg
	// 12% DC ---	2400  ---  180 deg

	// 5%  DC ---   700  ---  0   deg
	// 12% DC ---	2300  ---  180 deg
	int mintick = 690;
	int maxtick = 2565;
	float ticks_per_deg = (maxtick - mintick)/180.0;


	int t1 = mintick + (ticks_per_deg * a1);
	int t2 = mintick + (ticks_per_deg * a2);
	int t3 = mintick + (ticks_per_deg * a3);

	TIM1->CCR1 = t1;
	TIM1->CCR2 = t2;
	TIM1->CCR3 = t3;


}
